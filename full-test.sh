#!/bin/bash

BASE_URL="http://localhost:8181/api/notes"

echo "========================================="
echo "ПОЛНОЕ ТЕСТИРОВАНИЕ API NOTES SERVICE"
echo "Порт: 8181"
echo "========================================="
echo ""

# ==========================================
# ПОЗИТИВНЫЕ СЦЕНАРИИ
# ==========================================
echo "========================================="
echo "ПОЗИТИВНЫЕ СЦЕНАРИИ"
echo "========================================="

# 1. Создание заметки с одним тегом
echo -e "\n✅ 1. Создание заметки с одним тегом..."
curl -X POST $BASE_URL \
  -H "Content-Type: application/json" \
  -d '{"title":"Работа","content":"Собрание в 15:00","tags":["work"]}'
echo ""

# 2. Создание заметки с несколькими тегами
echo -e "\n✅ 2. Создание заметки с несколькими тегами..."
curl -X POST $BASE_URL \
  -H "Content-Type: application/json" \
  -d '{"title":"Личное","content":"Купить продукты","tags":["personal","shopping","home"]}'
echo ""

# 3. Создание заметки без тегов
echo -e "\n✅ 3. Создание заметки без тегов..."
curl -X POST $BASE_URL \
  -H "Content-Type: application/json" \
  -d '{"title":"Без тегов","content":"Простая заметка"}'
echo ""

# 4. Получение всех заметок
echo -e "\n✅ 4. Получение всех заметок..."
curl $BASE_URL
echo ""

# 5. Фильтрация по тегу 'work'
echo -e "\n✅ 5. Фильтрация по тегу 'work'..."
curl "$BASE_URL?tag=work"
echo ""

# 6. Фильтрация по тегу 'personal'
echo -e "\n✅ 6. Фильтрация по тегу 'personal'..."
curl "$BASE_URL?tag=personal"
echo ""

# 7. Получение заметки по ID (1)
echo -e "\n✅ 7. Получение заметки по ID 1..."
curl $BASE_URL/1
echo ""

# 8. Обновление заметки
echo -e "\n✅ 8. Обновление заметки..."
curl -X PUT $BASE_URL/1 \
  -H "Content-Type: application/json" \
  -d '{"title":"Обновленная работа","content":"Собрание в 16:00","tags":["work","updated"]}'
echo ""

# 9. Проверка обновленной заметки
echo -e "\n✅ 9. Проверка обновленной заметки..."
curl $BASE_URL/1
echo ""

# 10. Получение заметки по ID (2)
echo -e "\n✅ 10. Получение заметки по ID 2..."
curl $BASE_URL/2
echo ""

# 11. Удаление заметки
echo -e "\n✅ 11. Удаление заметки ID 1..."
curl -X DELETE $BASE_URL/1
echo ""

# 12. Проверка после удаления
echo -e "\n✅ 12. Все заметки после удаления..."
curl $BASE_URL
echo ""

# ==========================================
# НЕГАТИВНЫЕ СЦЕНАРИИ (ОШИБКИ)
# ==========================================
echo ""
echo "========================================="
echo "НЕГАТИВНЫЕ СЦЕНАРИИ (ОШИБКИ)"
echo "========================================="

# 13. Ошибка 404 - заметка не найдена
echo -e "\n❌ 13. Ошибка 404 - заметка не найдена..."
curl $BASE_URL/999
echo ""

# 14. Ошибка 400 - пустой заголовок
echo -e "\n❌ 14. Ошибка 400 - пустой заголовок..."
curl -X POST $BASE_URL \
  -H "Content-Type: application/json" \
  -d '{"title":"","content":"Содержимое"}'
echo ""

# 15. Ошибка 400 - пустое содержимое
echo -e "\n❌ 15. Ошибка 400 - пустое содержимое..."
curl -X POST $BASE_URL \
  -H "Content-Type: application/json" \
  -d '{"title":"Заголовок","content":""}'
echo ""

# 16. Ошибка 400 - слишком длинный заголовок (>100 символов)
echo -e "\n❌ 16. Ошибка 400 - слишком длинный заголовок..."
LONG_TITLE=$(printf 'A%.0s' {1..101})
curl -X POST $BASE_URL \
  -H "Content-Type: application/json" \
  -d "{\"title\":\"$LONG_TITLE\",\"content\":\"Содержимое\"}"
echo ""

# 17. Ошибка 400 - слишком длинное содержимое (>1000 символов)
echo -e "\n❌ 17. Ошибка 400 - слишком длинное содержимое..."
LONG_CONTENT=$(printf 'B%.0s' {1..1001})
curl -X POST $BASE_URL \
  -H "Content-Type: application/json" \
  -d "{\"title\":\"Заголовок\",\"content\":\"$LONG_CONTENT\"}"
echo ""

# 18. Ошибка 400 - слишком много тегов (>10)
echo -e "\n❌ 18. Ошибка 400 - слишком много тегов..."
curl -X POST $BASE_URL \
  -H "Content-Type: application/json" \
  -d '{"title":"Заголовок","content":"Содержимое","tags":["t1","t2","t3","t4","t5","t6","t7","t8","t9","t10","t11"]}'
echo ""

# 19. Ошибка 404 - обновление несуществующей заметки
echo -e "\n❌ 19. Ошибка 404 - обновление несуществующей заметки..."
curl -X PUT $BASE_URL/999 \
  -H "Content-Type: application/json" \
  -d '{"title":"Обновление","content":"Новое содержимое"}'
echo ""

# 20. Ошибка 404 - удаление несуществующей заметки
echo -e "\n❌ 20. Ошибка 404 - удаление несуществующей заметки..."
curl -X DELETE $BASE_URL/999
echo ""

# ==========================================
# ФИНАЛЬНАЯ ПРОВЕРКА
# ==========================================
echo ""
echo "========================================="
echo "ФИНАЛЬНАЯ ПРОВЕРКА"
echo "========================================="

echo -e "\n📊 21. Все заметки в системе..."
curl $BASE_URL
echo ""

echo -e "\n📊 22. Заметки с тегом 'work'..."
curl "$BASE_URL?tag=work"
echo ""

echo -e "\n📊 23. Заметки с тегом 'personal'..."
curl "$BASE_URL?tag=personal"
echo ""

echo -e "\n📊 24. Заметки с тегом 'shopping'..."
curl "$BASE_URL?tag=shopping"
echo ""

# ==========================================
# СТАТИСТИКА
# ==========================================
echo ""
echo "========================================="
echo "СТАТИСТИКА ТЕСТИРОВАНИЯ"
echo "========================================="

echo "Всего тестов: 24"
echo ""
echo "Ожидаемые коды ответов:"
echo "  - 200 OK (успешный GET/PUT)"
echo "  - 201 Created (успешный POST)"
echo "  - 204 No Content (успешный DELETE)"
echo "  - 400 Bad Request (ошибка валидации)"
echo "  - 404 Not Found (ресурс не найден)"

echo ""
echo "========================================="
echo "✅ ТЕСТИРОВАНИЕ ЗАВЕРШЕНО!"
echo "========================================="
